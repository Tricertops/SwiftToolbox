// Use this playground to try the package.

let pi = Double.pi

"\(pi)"  // "3.141592653589793"
"\(pi, .pretty)"  // "3.14159"
"\(pi, .percent)"  // "314%"
"\(pi, .degrees)"  // "180°"

"\(pi)"  // "3.141592653589793"
"\(pi, (~))"  // "3.14159"
"\(pi, (%))"  // "314%"
"\(pi, (°))"  // "180°"

"\(pi)"  // "3.141592653589793"
"\(pi, 0)"  // "3"
"\(pi, 3)"  // "3.142"
"\(pi, 6)"  // "3.141593"

"\(pi)"  // "3.141592653589793"
"\(pi, %, 1)"  // "314.2%"
"\(pi, °, 2)"  // "180.00°"

"\(pi)"  // "3.141592653589793"
"\(pi, .percent, 1)"  // "314.2%"
"\(pi, .degrees, 2)"  // "180.00°"
